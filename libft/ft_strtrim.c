#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	i;
	size_t	j;
	char	*buffer;

	buffer = 0;
	if (s1 != 0 && set != 0)
	{
		i = 0;
		j = ft_strlen(s1);
		while (s1[i] && ft_strchr(set, s1[i]))
			i++;
		while (s1[j - 1] && ft_strchr(set, s1[j - 1]) && j > i)
			j--;
		buffer = (char *)malloc(sizeof(char) * (j - i + 1));
		if (buffer)
			ft_strlcpy(buffer, &s1[i], j - i + 1);
	}
	return (buffer);
}
