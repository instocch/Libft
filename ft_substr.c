#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*newstring;
	size_t	slen;
	size_t	finish;

	if (!s)
		return (0);
	slen = ft_strlen(s);
	finish = 0;
	if (start < slen)
		finish = slen - start;
	if (finish > len)
		finish = len;
	newstring = (char *)malloc(sizeof(char) * (finish + 1));
	if (!newstring)
		return (0);
	ft_strlcpy(newstring, s + start, finish + 1);
	return (newstring);
}
